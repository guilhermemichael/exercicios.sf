#1. Média Aritmética: Peça três notas de um aluno e calcule a média final.

print("Faça sua Média Aritmética")

n1 = float(input("Digite a primeira nota: "))
n2 = float(input("Digite a segunda nota: "))
n3 = float(input("Digite a terceira nota: "))

mf = (n1 + n2 + n3) / 3

print(f"A média final é: {mf:.2f}")
