# 3. Sucessor e Antecessor: Leia um número inteiro e mostre na tela o seu antecessor e o seu sucessor.

print("Sucessor e Antecessor")

n1 = int(input("Digite um número inteiro para saber seu antecessor e sucessor: "))

print(f"\nNúmero: {n1}")
print(f"Antecessor: {n1 - 1}")
print(f"Sucessor: {n1 + 1}")
