#8. Calculadora de Dias: Peça a idade de uma pessoa apenas em anos e exiba aproximadamente quantos dias ela já viveu (considere anos de 365 dias).

print("Calculadora de Dias")

a = int(input("Digite a idade de uma pessoa em anos: "))
d = a * 365

print(f"\nIdade: {a} anos")
print(f"\nJá viveu aproximadamente {d} dias")
